extern int	  has_cpuid		();
extern void  get_cpu_feat	(unsigned int *out_regs);

int getCPUInfos(cpu_infos_t *cpu_infos)
{
	unsigned int out_regs[4];

	cpu_infos->sse_ver		=0;
	cpu_infos->cache_line	=1;	

	if(!has_cpuid		())return 0;
	
	get_cpu_feat	(out_regs);

	if(out_regs[2]&CPUID_FEAT_ECX_SSE4_2)
	{
		cpu_infos->sse_ver		=4;
		cpu_infos->sse_sub_ver	=2;
	}
	else if(out_regs[2]&CPUID_FEAT_ECX_SSE4_1)
	{
		cpu_infos->sse_ver		=4;
		cpu_infos->sse_sub_ver	=1;
	}
	else if(out_regs[2]&CPUID_FEAT_ECX_SSE3)
	{
		cpu_infos->sse_ver		=3;
		cpu_infos->sse_sub_ver	=0;
	}
	else if(out_regs[3]&CPUID_FEAT_EDX_SSE2)
	{
		cpu_infos->sse_ver		=2;
		cpu_infos->sse_sub_ver	=0;
	}
	else if(out_regs[3]&CPUID_FEAT_EDX_SSE)
	{
		cpu_infos->sse_ver		=1;
		cpu_infos->sse_sub_ver	=0;
	}
	return 1;

	

}